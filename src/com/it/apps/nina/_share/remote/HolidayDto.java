package com.it.apps.nina._share.remote;

public class HolidayDto {
	
	protected String lunarType;
	
	protected String monthDay;
	
	protected String addUp;
	
	protected String legalType;

	public String getLunarType() {
		return lunarType;
	}

	public void setLunarType(String lunarType) {
		this.lunarType = lunarType;
	}

	public String getMonthDay() {
		return monthDay;
	}

	public void setMonthDay(String monthDay) {
		this.monthDay = monthDay;
	}

	public String getAddUp() {
		return addUp;
	}

	public void setAddUp(String addUp) {
		this.addUp = addUp;
	}

	public String getLegalType() {
		return legalType;
	}

	public void setLegalType(String legalType) {
		this.legalType = legalType;
	}

}
